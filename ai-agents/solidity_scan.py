"""
Solidity-scan — общий помощник статического разбора `.sol` для AI-агентов.

СЛУЖЕБНЫЙ МОДУЛЬ, НЕ ОРГАН ВЛАСТИ (Конституция, ст. 9). Ничего не двигает и
ничем не владеет — это библиотека текстового разбора Solidity, которую
переиспользуют read-only агенты (Reputation, Housing и будущие по контрактам),
чтобы доказывать конституционные инварианты «в коде, а не на словах» и при этом
НЕ расходиться в логике парсинга.

Почему отдельный модуль: одни и те же помощники (`strip_solidity_comments`,
`function_body`, `function_signature`) копировались по агентам — три копии легко
тихо разойдутся. Здесь они в одном месте, с собственным тест-инвариантом
`test_solidity_scan.py`.

Важно: это НЕ полноценный парсер Solidity, а лёгкий текстовый разбор «достаточно
для инвариантов». Перед любой проверкой вырезаются комментарии — иначе агент
ложно краснел бы на пояснениях в самом контракте (например, комментарий
«у контракта НЕТ функций transfer/approve/transferFrom»).

Зависимостей нет — только стандартная библиотека (детерминированно в любом CI).
"""

import re


def strip_solidity_comments(src):
    """Убирает // и /* */ комментарии — чтобы не ловить упоминания в пояснениях.

    Например, в `Reputation.sol` есть комментарий «У контракта НЕТ функций
    transfer/approve/transferFrom» — без вырезания комментариев проверка
    soulbound ложно бы краснела на собственном пояснении контракта.
    """
    # Блочные комментарии /* ... */ (в т.ч. многострочные).
    src = re.sub(r"/\*.*?\*/", " ", src, flags=re.DOTALL)
    # Строчные комментарии // ... до конца строки.
    src = re.sub(r"//[^\n]*", " ", src)
    return src


def has_function(code, fn):
    """True, если в коде объявлена функция с именем fn (`function <fn> ( ...`).

    Совпадает только как ИМЯ функции (после ключевого слова `function`), а не
    как подстрока в другом идентификаторе — `\\bfunction\\s+<fn>\\s*\\(`.
    """
    return bool(re.search(r"\bfunction\s+" + re.escape(fn) + r"\s*\(", code))


def function_signature(code, fn):
    """Сигнатура функции fn: {"params", "modifiers"} (между именем и '{') или None.

    `params`   — содержимое скобок объявления (аргументы);
    `modifiers`— всё между `)` и `{` (видимость, mutability, returns, модификаторы).
    """
    m = re.search(
        r"\bfunction\s+" + re.escape(fn) + r"\s*\(([^)]*)\)([^{;]*)\{",
        code, flags=re.DOTALL,
    )
    if not m:
        return None
    return {"params": m.group(1), "modifiers": m.group(2)}


def function_body(code, fn):
    """Тело функции fn по балансу фигурных скобок (или None, если не найдена).

    Регуляркой `.*?` тело не вырезать: первая же внутренняя `}` (конец вложенного
    `if`/блока) оборвала бы захват. Поэтому считаем баланс скобок от открывающей
    `{` тела функции до парной закрывающей.
    """
    m = re.search(r"\bfunction\s+" + re.escape(fn) + r"\s*\([^)]*\)[^{;]*\{", code)
    if not m:
        return None
    i = m.end()  # позиция сразу после открывающей `{` тела функции
    depth = 1
    while i < len(code) and depth:
        if code[i] == "{":
            depth += 1
        elif code[i] == "}":
            depth -= 1
        i += 1
    return code[m.end():i - 1]
