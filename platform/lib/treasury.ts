// Сколько казна готова распределить прямо сейчас (помимо запаса).
export function distributable(s: TreasurySnapshot): number {
  return s.helpPool + s.rewardPool;
}

// Доля части от целого в процентах — для понятных полосок. Без дробей: людям проще.
export function sharePercent(part: number, whole: number): number {
  if (whole <= 0) return 0;
  return Math.round((part / whole) * 100);
}

// Понятная запись суммы: группируем тысячи неразрывным пробелом, без копеек.
// Пример: 12000 → «12 000».
export function formatAmount(value: number): string {
  const rounded = Math.round(value);
  const sign = rounded < 0 ? "−" : "";
  const digits = Math.abs(rounded).toString();
  let grouped = "";
  for (let i = 0; i < digits.length; i++) {
    if (i > 0 && (digits.length - i) % 3 === 0) grouped += " ";
    grouped += digits[i];
  }
  return sign + grouped;
}

// Знак движения для подписи (поступление со знаком «+», расход со знаком «−»).
export function movementSign(kind: MovementKind): "+" | "−" {
  return kind === "in" ? "+" : "−";
}
