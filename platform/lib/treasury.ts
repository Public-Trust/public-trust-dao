// Слой данных для экрана «Окно казны».
//
// Простыми словами: здесь живёт устройство общей казны — сколько в ней всего,
// сколько отложено в неприкосновенный запас, сколько готово пойти на прямую
// помощь людям и сколько — на скромную благодарность за работу. Экран только
// показывает это состояние: потратить отсюда ничего нельзя (read-only).
//
// Правила, по которым казна делит средства, заданы в docs/REWARDS-MODEL.md:
// сначала бережётся неприкосновенный запас, затем остаток делится так, что на
// прямую помощь людям всегда идёт не меньше 70%, а благодарность за работу
// держится в пределах потолка. Чем лучше фонд обеспечен, тем шире помощь.
//
// Пока умные контракты не запущены в тестовой сети, экран показывает
// ПОКАЗАТЕЛЬНОЕ состояние (пример) и показательные движения — чтобы экран
// работал уже сейчас. Все суммы — в тестовых единицах, реальных денег нет.
// Настоящие данные появятся, когда оператор развернёт контракты в тестовой
// сети: тот же экран переключат на чтение «вживую» без переписывания (слой
// данных намеренно отделён от экрана).

// Насколько фонд обеспечен — простыми словами, без формул. Человеческие подписи
// к этим уровням живут на двух языках в lib/i18n.ts (правило понятного языка
// PTD-0040). От уровня зависит, насколько широкой может быть помощь.
//   scarce — скудно: помощь только самому критичному, благодарность минимальна;
//   modest — скромно: базовая нужда и самая важная работа;
//   good   — хорошо: помощь шире, благодарность в нормальных рамках;
//   ample  — с запасом: широкая помощь и заметная, но ограниченная благодарность.
export type TreasuryCoverage = "scarce" | "modest" | "good" | "ample";

// Снимок состояния казны. Суммы в тестовых единицах (подпись единицы — в i18n).
// Договорённость: balance = reserve + helpPool + rewardPool (всё сходится).
export type TreasurySnapshot = {
  network: "testnet";
  asOf: string; // дата снимка (показательная)
  balance: number; // всего в казне
  reserve: number; // неприкосновенный запас — его не трогают
  helpPool: number; // отложено на прямую помощь людям
  rewardPool: number; // отложено на благодарность за работу
  coverage: TreasuryCoverage;
};

// Показательное состояние казны (пример). Помощь = 75% распределяемого остатка
// (выше конституционного минимума 70%), благодарность = 25% (ниже потолка ≈30%).
