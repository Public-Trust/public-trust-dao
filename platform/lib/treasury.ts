// Вид движения средств.
//   in     — поступление (пожертвование в казну);
//   help   — прямая помощь человеку (оплата нужды напрямую поставщику);
//   reward — скромная благодарность за работу для фонда.
export type MovementKind = "in" | "help" | "reward";

// Одно движение средств. Тексты (на что/от кого) — в i18n по этому id.
export type Movement = {
  id: string;
  kind: MovementKind;
  amount: number;
  date: string;
};

// Показательные движения (примеры) — новые сверху. Прямая помощь всегда
